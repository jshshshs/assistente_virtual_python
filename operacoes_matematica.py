import io
import math

class operacoes_matematica():

    def _init_(self):
        pass

    def sum(self, x, y):
        return x+y

    def sub(self, x, y):
        return x-y

    def mult(self, x, y):
        return x*y

    def div(self, x, y):
        return x/y

    def raiz(self, x):

        calc = math.sqrt(x)
        return calc

       
            
